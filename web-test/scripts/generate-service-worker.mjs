import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const projectRoot = path.resolve(path.dirname(__filename), "..");
const webRoot = path.join(projectRoot, "web");
const serviceWorkerPath = path.join(webRoot, "service-worker.js");

const ignored = new Set([
  "service-worker.js"
]);

const offlineFiles = ["./"];

function walk(directory) {
  for (const entry of fs.readdirSync(directory, { withFileTypes: true })) {
    const absolutePath = path.join(directory, entry.name);
    const relativeFromWeb = path.relative(webRoot, absolutePath).replaceAll(path.sep, "/");

    if (ignored.has(relativeFromWeb)) {
      continue;
    }

    if (entry.isDirectory()) {
      walk(absolutePath);
    } else {
      offlineFiles.push(`./${relativeFromWeb}`);
    }
  }
}

walk(webRoot);

offlineFiles.sort();

const contents = `// Auto-generated by scripts/generate-service-worker.mjs.\nconst CACHE_NAME = "rehab-ai-pose-test-v1";\nconst OFFLINE_FILES = ${JSON.stringify(offlineFiles, null, 2)};\n\nself.addEventListener("install", event => {\n  event.waitUntil(\n    caches.open(CACHE_NAME).then(cache => cache.addAll(OFFLINE_FILES))\n  );\n  self.skipWaiting();\n});\n\nself.addEventListener("activate", event => {\n  event.waitUntil(\n    caches.keys().then(cacheNames =>\n      Promise.all(\n        cacheNames\n          .filter(cacheName => cacheName !== CACHE_NAME)\n          .map(cacheName => caches.delete(cacheName))\n      )\n    )\n  );\n  self.clients.claim();\n});\n\nself.addEventListener("fetch", event => {\n  if (event.request.method !== "GET") {\n    return;\n  }\n\n  event.respondWith(\n    caches.match(event.request).then(cachedResponse => {\n      return cachedResponse || fetch(event.request);\n    })\n  );\n});\n`;

fs.writeFileSync(serviceWorkerPath, contents);
console.log(`Generated service worker with ${offlineFiles.length} offline files.`);

const modelPath = path.join(webRoot, "models", "pose_landmarker_lite.task");
if (!fs.existsSync(modelPath)) {
  console.warn("Warning: web/models/pose_landmarker_lite.task is missing.");
  console.warn("Copy your Lite model there before testing offline.");
}
