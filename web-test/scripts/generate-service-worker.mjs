import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const projectRoot = path.resolve(path.dirname(__filename), "..");
const webRoot = path.join(projectRoot, "web");
const serviceWorkerPath = path.join(webRoot, "service-worker.js");
const buildId =
  process.env.GITHUB_SHA?.slice(0, 12) ?? Date.now().toString();

const cacheName = `rehab-ai-pose-test-${buildId}`;

const ignored = new Set([
  "service-worker.js"
]);

const offlineFiles = ["./"];

function walk(directory) {
  for (const entry of fs.readdirSync(directory, { withFileTypes: true })) {
    const absolutePath = path.join(directory, entry.name);
    const relativeFromWeb = path.relative(webRoot, absolutePath).replaceAll(path.sep, "/");

    if (ignored.has(relativeFromWeb)) {
      continue;
    }

    if (entry.isDirectory()) {
      walk(absolutePath);
    } else {
      offlineFiles.push(`./${relativeFromWeb}`);
    }
  }
}

walk(webRoot);

offlineFiles.sort();

const contents = `// Auto-generated by scripts/generate-service-worker.mjs.
const CACHE_NAME = "${cacheName}";
const OFFLINE_FILES = ${JSON.stringify(offlineFiles, null, 2)};

self.addEventListener("install", event => {
  event.waitUntil(
    caches
      .open(CACHE_NAME)
      .then(cache => cache.addAll(OFFLINE_FILES))
      .then(() => self.skipWaiting())
  );
});

self.addEventListener("activate", event => {
  event.waitUntil(
    caches.keys()
      .then(cacheNames =>
        Promise.all(
          cacheNames
            .filter(cacheName => cacheName !== CACHE_NAME)
            .map(cacheName => caches.delete(cacheName))
        )
      )
      .then(() => self.clients.claim())
  );
});

self.addEventListener("fetch", event => {
  if (event.request.method !== "GET") {
    return;
  }

  event.respondWith(
    fetch(event.request)
      .then(networkResponse => {
        const copy = networkResponse.clone();

        caches.open(CACHE_NAME).then(cache => {
          cache.put(event.request, copy);
        });

        return networkResponse;
      })
      .catch(() => caches.match(event.request))
  );
});
`;

fs.writeFileSync(serviceWorkerPath, contents);
console.log(`Generated service worker with ${offlineFiles.length} offline files.`);

const modelPath = path.join(webRoot, "models", "pose_landmarker_heavy.task");
if (!fs.existsSync(modelPath)) {
  console.warn("Warning: web/models/pose_landmarker_heavy.task is missing.");
  console.warn("Copy your Lite model there before testing offline.");
}
